text = "PythonProgramming"

print("Original String:", text)
print("Slice from index 0 to 6:", text[0:6])
print("Slice from index 6 to end:", text[6:])
print("Slice between index 2 and 10:", text[2:10])
print("Slice with step of 2:", text[::2])
print("Last 5 characters:", text[-5:])
print("Reverse string:", text[::-1])