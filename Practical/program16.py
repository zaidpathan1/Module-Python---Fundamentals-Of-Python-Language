text = "  hello world  "

print("Original String:", text)
print("Uppercase:", text.upper())
print("Lowercase:", text.lower())
print("Title Case:", text.title())
print("Stripped (no spaces):", text.strip())
print("Replaced 'world' with 'Python':", text.replace("world", "Python"))
print("Count of 'l':", text.count('l'))
print("Starts with 'h':", text.strip().startswith('h'))
print("Ends with 'd':", text.strip().endswith('d'))
print("Split into words:", text.strip().split())